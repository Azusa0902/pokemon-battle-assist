// 🟢 攻撃タイプと防御タイプ（複合対応）を渡して倍率を計算する関数
export const getEffectiveness = (attackType: string, defendType1: string, defendType2: string = '') => {
  if (!attackType) return 1;
  let multiplier = 1;
  
  if (TYPE_CHART[attackType]) {
    if (defendType1 && TYPE_CHART[attackType][defendType1] !== undefined) {
      multiplier *= TYPE_CHART[attackType][defendType1];
    }
    if (defendType2 && TYPE_CHART[attackType][defendType2] !== undefined) {
      multiplier *= TYPE_CHART[attackType][defendType2];
    }
  }
  return multiplier;
};